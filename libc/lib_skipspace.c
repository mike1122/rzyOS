#include <string.h>
#include <ctype.h>
#include <libc.h>

/****************************************************************************
 * Private Functions
 ****************************************************************************/

/****************************************************************************
 * Public Functions
 ****************************************************************************/

/****************************************************************************
 * Name: lib_skipspace
 *
 * Description:
 *   Skip over leading whitespace
 *
 ****************************************************************************/

void lib_skipspace(const char **pptr)
{
	const char *ptr = *pptr;
	while (isspace(*ptr))
	{
		ptr++;
	}
	*pptr = ptr;
}
